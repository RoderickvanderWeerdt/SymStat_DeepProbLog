/*------ins and in----------------------------------------------*/

in(H, [H|_]).
in(V, [_|T]) :-
        in(V, T).

ins([H],Dom,[H2]):-
  in(H, Dom),
  H2 = H.

ins([H|T], Dom, [H2|T2]) :-
        in(H, Dom),
        H2 = H,
        ins(T, Dom, T2).

/* ----------- XXX https://eclipseclp.org/doc/bips/lib/lists/memberchk-2.html------------------------- */
memberchk(X,[X|_]) :- true.
memberchk(X,[_|T]):- memberchk(X,T).

unique([]).
unique([X|Xs]) :- \+ memberchk(X, Xs), unique(Xs).

/*----------------------------------------------*/

all_distinct(_, []).

all_distinct(H, [HH|T]) :-
        H \= HH,
        all_distinct(H, T).

all_distinct([]).
all_distinct([H|T]) :-
        all_distinct(H, T),
        all_distinct(T).
